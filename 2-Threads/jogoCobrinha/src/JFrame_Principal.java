    void pegou() {
        if (jButton_cobrinha.getBounds().intersects(jButton_fruta.getBounds())) {

            VELOCIDADE = VELOCIDADE - 10;
            if (VELOCIDADE <= 30) {
                VELOCIDADE = 30;
            }

            jButton_fruta.setBounds(gerador.nextInt(jPanel_principal.getWidth() - jButton_cobrinha.getBounds().width),
                    gerador.nextInt(jPanel_principal.getHeight() - jButton_cobrinha.getBounds().height),
                    jButton_fruta.getBounds().width, jButton_fruta.getBounds().height);

            listaCauda.add(new JButton());

            for (int i = 0; i < listaCauda.size(); i++) {
                jPanel_principal.add((JButton) listaCauda.get(listaCauda.size() - 1));
                ((JButton) listaCauda.get(listaCauda.size() - 1)).setBounds(jButton_cobrinha.getX(), jButton_cobrinha.getY(), jButton_cobrinha.getBounds().width, jButton_cobrinha.getBounds().height);
            }

        }
    }

