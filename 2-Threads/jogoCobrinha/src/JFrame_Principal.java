    private void jButton_cobrinhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jButton_cobrinhaKeyPressed
        // TODO add your handling code here:

        switch (evt.getKeyCode()) {
            case KeyEvent.VK_W:
                if (threadBotao.isAlive()) {
                    threadBotao.suspend();
                }
                threadBotao = new Thread() {
                    public void run() {
                        paraCima();
                    }
                };
                threadBotao.start();
                break;

            case KeyEvent.VK_S:
                if (threadBotao.isAlive()) {
                    threadBotao.suspend();
                    //System.out.println("suspendendo a thread");
                }
                threadBotao = new Thread() {
                    public void run() {
                        paraBaixo();
                    }
                };
                threadBotao.start();
                break;

            case KeyEvent.VK_A:
                if (threadBotao.isAlive()) {
                    threadBotao.suspend();
                    //System.out.println("suspendendo a thread");
                }
                threadBotao = new Thread() {
                    public void run() {
                        paraEsquerda();
                    }
                };
                threadBotao.start();
                break;

            case KeyEvent.VK_D:
                if (threadBotao.isAlive()) {
                    threadBotao.suspend();
                    //System.out.println("suspendendo a thread");
                }
                threadBotao = new Thread() {
                    public void run() {
                        paraDireita();
                    }
                };
                threadBotao.start();
                break;
        }
    }//GEN-LAST:event_jButton_cobrinhaKeyPressed

    void paraCima() {
        do {
            try {
                this.jButton_cobrinha.setBounds(this.jButton_cobrinha.getX(),
                        this.jButton_cobrinha.getY() - 10,
                        this.jButton_cobrinha.getBounds().width, this.jButton_cobrinha.getBounds().width);
                pegou();
                //perseguir
                 if (!listaCauda.isEmpty()) {
                    for (int i = 0; i < listaCauda.size(); i++) {
                        if (i == 0) {
                            ((JButton) listaCauda.get(0)).setBounds(jButton_cobrinha.getX(), jButton_cobrinha.getY() + jButton_cobrinha.getHeight(), jButton_cobrinha.getBounds().width, jButton_cobrinha.getBounds().height);
                        } else {
                            ((JButton) listaCauda.get(i)).setBounds(((JButton) listaCauda.get(i - 1)).getX(), ((JButton) listaCauda.get(i - 1)).getY() + jButton_cobrinha.getHeight() , jButton_cobrinha.getBounds().width, jButton_cobrinha.getBounds().height);
                        }
                    }
                }
                Thread.sleep(VELOCIDADE);

                if (this.jButton_cobrinha.getY() <= 10) {
                    break;
                }
            } catch (InterruptedException ex) {
                Logger.getLogger(JFrame_Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        } while (true);
    }

    void paraBaixo() {
        do {
            try {
                this.jButton_cobrinha.setBounds(this.jButton_cobrinha.getX(),
                        this.jButton_cobrinha.getY() + 10,
                        this.jButton_cobrinha.getBounds().width, this.jButton_cobrinha.getBounds().width);
                pegou();
                //perseguir
                if (!listaCauda.isEmpty()) {
                    for (int i = 0; i < listaCauda.size(); i++) {
                        if (i == 0) {
                            ((JButton) listaCauda.get(0)).setBounds(jButton_cobrinha.getX(), jButton_cobrinha.getY() - jButton_cobrinha.getHeight(), jButton_cobrinha.getBounds().width, jButton_cobrinha.getBounds().height);
                        } else {
                            ((JButton) listaCauda.get(i)).setBounds(((JButton) listaCauda.get(i - 1)).getX(), ((JButton) listaCauda.get(i - 1)).getY() - jButton_cobrinha.getHeight() , jButton_cobrinha.getBounds().width, jButton_cobrinha.getBounds().height);
                        }
                    }
                }
                Thread.sleep(VELOCIDADE);

                if (this.jButton_cobrinha.getY() >= jPanel_principal.getHeight() - jButton_cobrinha.getBounds().height) {
                    break;
                }
            } catch (InterruptedException ex) {
                Logger.getLogger(JFrame_Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        } while (true);
    }

