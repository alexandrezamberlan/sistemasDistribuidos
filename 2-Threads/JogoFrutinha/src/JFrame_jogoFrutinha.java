    public void verificaTeclas() {
        for (Integer c : pressed) {
            switch (c) {
                case KeyEvent.VK_UP:
                    new Thread() {
                        @Override
                        public void run() {
                            Movimenta.irCima(jButton_jogador2);
                        }
                    }.start();
                    break;
                case KeyEvent.VK_DOWN:
                    new Thread() {
                        @Override
                        public void run() {
                            Movimenta.irBaixo(jButton_jogador2, altura);
                        }
                    }.start();
                    break;
                case KeyEvent.VK_LEFT:
                    new Thread() {
                        @Override
                        public void run() {
                            Movimenta.irEsquerda(jButton_jogador2);
                        }
                    }.start();
                    break;
                case KeyEvent.VK_RIGHT:
                    new Thread() {
                        @Override
                        public void run() {
                            Movimenta.irDireita(jButton_jogador2, largura);
                        }
                    }.start();
                    break;
                case KeyEvent.VK_W:
                    new Thread() {
                        @Override
                        public void run() {
                            Movimenta.irCima(jButton_jogador1);
                        }
                    }.start();
                    break;
                case KeyEvent.VK_S:
                    new Thread() {
                        @Override
                        public void run() {
                            Movimenta.irBaixo(jButton_jogador1, altura);
                        }
                    }.start();
                    break;
                case KeyEvent.VK_A:
                    new Thread() {
                        @Override
                        public void run() {
                            Movimenta.irEsquerda(jButton_jogador1);
                        }
                    }.start();
                    break;
                case KeyEvent.VK_D:
                    new Thread() {
                        @Override
                        public void run() {
                            Movimenta.irDireita(jButton_jogador1, largura);
                        }
                    }.start();
                    break;
            }
        }
        if (Movimenta.pegou(jButton_fruta, jButton_jogador1)) {
            Movimenta.sorteiaPosicao(jButton_fruta, meuFrame);
            pontosJogador1++;
            jTextField_jogador1.setText("" + pontosJogador1);
        }
        if (Movimenta.pegou(jButton_fruta, jButton_jogador2)) {
            Movimenta.sorteiaPosicao(jButton_fruta, meuFrame);
            pontosJogador2++;
            jTextField_jogador2.setText("" + pontosJogador2);
        }
    }
